package Day_27_Arrays_part4;

public class RaggedArray {

	public static void main(String[] args) {
		
		int [][] array=new int[4][]; // arrayin 4 roe oldupunu belirttik ama henüz column u belirtmedik
		
		System.out.println(array[0]);
		
		
		array[0]=new int[3];  // row-0 ın 3 adet column a sahip olduğpunu belirttik
		array[1]=new int[4];  // row-1 ın 4 adet column a sahip olduğpunu belirttik
		array[2]=new int[2];  // row-2 ın 2 adet column a sahip olduğpunu belirttik
		array[3]=new int[3];  // row-3 ın 3 adet column a sahip olduğpunu belirttik
		
		
		
		
	}

}
